'use strict';
const fs = require('fs/promises');
const path = require('path');
const crypto = require('crypto');
const zlib = require('zlib');
const { promisify } = require('util');
const { APP_COLLECTION_KEYS } = require('../../src/constants/collectionKeys');
const { buildBackupIntegrity } = require('../../src/operations/backupIntegrity');
const gzip = promisify(zlib.gzip);
(async () => {
  const dir = path.resolve('artifacts/prompt11-after/restore-fixture');
  await fs.rm(dir, {recursive:true, force:true});
  await fs.mkdir(dir, {recursive:true});
  const data = Object.fromEntries(APP_COLLECTION_KEYS.map(k => [k, []]));
  data.products = [{ code:'TEST-001', name:'Restore Drill Product', salePrice:10000, baseUnit:'Gói', conversionRate:1 }];
  data.customers = [{ code:'CUS-001', name:'Restore Drill Customer' }];
  data.users = [{ username:'restore-drill', passwordHash:'$2b$12$123456789012345678901u1234567890123456789012345678901234', role:'admin', active:true }];
  data.salesOrders = [{ id:'SO-DRILL-001', code:'SO-DRILL-001', customerCode:'CUS-001', customerName:'Restore Drill Customer', status:'pending', lifecycleStatus:'pending', items:[], total:10000 }];
  data.returnOrders = [{ id:'RO-DRILL-001', code:'RO-DRILL-001', salesOrderCode:'SO-DRILL-001', status:'pending', items:[] }];
  data.inventories = [{ productCode:'TEST-001', warehouseCode:'MAIN', onHand:12, availableQty:12 }];
  data.arLedgers = [{ id:'AR-DRILL-001', account:'AR', debit:10000, credit:2000, status:'active' }];
  data.fundLedgers = [{ id:'FUND-DRILL-001', direction:'in', amount:2000, status:'active' }];
  const createdAt = new Date().toISOString();
  const counts = Object.fromEntries(Object.entries(data).map(([k,v])=>[k,v.length]));
  const payload = { format:'mk-pro-backup-v2', createdAt, source:'isolated-fixture', counts, integrity:buildBackupIntegrity(data), data };
  const compressed = await gzip(Buffer.from(JSON.stringify(payload)), {level:zlib.constants.Z_BEST_SPEED});
  const fileName = `backup-${createdAt.replace(/[:.]/g,'-')}.json.gz`;
  const sha = crypto.createHash('sha256').update(compressed).digest('hex');
  await fs.writeFile(path.join(dir,fileName), compressed);
  await fs.writeFile(path.join(dir,`${fileName}.sha256`), `${sha}  ${fileName}\n`);
  await fs.writeFile(path.join(dir,'fixture-meta.json'), JSON.stringify({fileName,sha,sizeBytes:compressed.length},null,2));
  console.log(fileName);
})().catch(e=>{console.error(e); process.exit(1)});
